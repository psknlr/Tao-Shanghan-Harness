    fun abstractTemplate(topic: String, n: Int, topSymptoms: List<String>,
                         topHerbs: List<String>, mainChannel: String): String =
        "目的：基于《伤寒论》宋本条文语料，系统梳理「$topic」相关条文的" +
            "证候结构、用药规律与六经分布。方法：以简繁归一与异体字折叠后的" +
            "全文检索合并 BM25 召回，检得相关条文 $n 条；统计症状频次、" +
            "药物频次、同方药对共现与六经归属，全部计算端侧可复现。" +
            "结果：高频证候为${topSymptoms.take(3).joinToString("、")}等；" +
            "高频药物为${topHerbs.take(3).joinToString("、")}等；" +
            "条文分布以${mainChannel}为主。结论：「$topic」的证治规律呈现" +
            "以${mainChannel}为核心的病机结构，其配伍与加减法度可从" +
            "高频药对与支持条文逐条回源验证。"

    fun introTemplate(topic: String, n: Int): String =
        "《伤寒论》为方书之祖，六经辨证体系奠定了中医辨证论治之圭臬。" +
            "「$topic」相关证治散见于诸篇条文，历代注家阐发不一，" +
            "然多凭个案与经验，缺乏对条文全集的系统计量。本文以宋本 398 条" +
            "核心条文为语料，检得相关条文 $n 条，试以文献计量方法刻画其" +
            "证候—方药—六经结构，为经方研究提供可回源、可复核的数据基础。" +
            "（本段为模板骨架，建议用「AI 撰写」结合统计生成后人工审定。）"

    fun methodsTemplate(topic: String): String =
        "1. 语料：《伤寒论》宋本条文版（SHL_SONGBEN 编号 1–398）及附篇，" +
            "共 681 条记录，语料指纹随包可验。2. 检索：以「$topic」为检索词，" +
            "经简繁归一（s2t）与异体字折叠（脇→脅、鞕→硬等）后，在条文正文、" +
            "证候要素与方名字段做包含匹配，并与字符级 BM25（k1=1.5, b=0.75）" +
            "召回合并。3. 统计：对命中条文计数症状频次、方剂块内药物频次、" +
            "同方药对共现与六经归属；全部为确定性计算，结果可端侧复现。"

    fun discussionSkeleton(
        topic: String,
        channelDist: List<Pair<String, Int>>,
        herbPairs: List<Pair<String, Int>>,
        topSymptoms: List<String>,
    ): List<String> {
        val out = ArrayList<String>()
        val main = channelDist.take(2)
        for ((ch, n) in main) {
            val theory = CHANNEL_THEORY[ch]
            if (theory != null) {
                out += "（${ch}病机与「$topic」）$theory 本研究中「$topic」" +
                    "相关条文有 $n 条归属$ch，其高频证候" +
                    "（${topSymptoms.take(3).joinToString("、")}）与上述病机" +
                    "框架相合，提示其证治重心所在。"
            }
        }
        if (herbPairs.isNotEmpty()) {
            out += "（配伍结构）高频药对" +
                herbPairs.take(3).joinToString("、") {
                    "${it.first}（${it.second} 次）"
                } +
                "提示核心药组的稳定配伍关系。经方药简义精，药对之增损即" +
                "法度之进退——如桂枝配甘草辛甘化阳、芍药配甘草酸甘化阴——" +
                "本组高频药对的气味配伍与主治病机的对应关系，宜结合具体" +
                "条文加减逐一考订。"
        }
        out += "（局限）本研究为条文语料的计量描述：其一，频次不等于权重，" +
            "关键条文可能低频而义重；其二，方证对应以条文明言为准，" +
            "后世归纳（D 层）与模型解释（E 层）未计入；其三，历代注家" +
            "分歧与版本异文的影响需在证据分层框架下另行考察。"
        return out
    }

    fun conclusionTemplate(topic: String, mainChannel: String): String =
        "「$topic」相关条文以${mainChannel}为病机重心，高频证候与高频药对" +
            "呈现稳定的证—治对应结构。基于可回源条文编号的计量描述，" +
            "可为经方医学的教学与研究提供确定性的数据底座；" +
            "临证运用仍须四诊合参，由执业医师当面判断。"
}
