package org.impfai.hermes.engine

/**
 * 論文寫作的中醫理論模板庫（端側確定性文案，數據佔位由統計填入）。
 * 理論表述取自《傷寒論》六經辨證通行學術口徑；模板僅提供論述骨架，
 * 每段落都要求作者回源證據條文後定稿。
 */
object PaperTheory {

    /** 六經病機總論（討論章按主經選段嵌入）。 */
